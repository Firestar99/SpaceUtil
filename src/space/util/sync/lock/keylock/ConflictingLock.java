package space.util.sync.lock.keylock;

import space.util.indexmap.IndexMap;
import space.util.indexmap.IndexMapArray;
import space.util.indexmap.multi.IndexMultiMap;
import space.util.indexmap.multi.IndexMultiMap2D;
import space.util.keygen.IKey;
import space.util.keygen.IKeyGenerator;
import space.util.keygen.IllegalKeyException;
import space.util.sync.awaitable.IAwaitable;
import space.util.sync.awaitable.LockLikeSignalable;

import java.util.concurrent.TimeUnit;

/**
 * The conflicting Lock is an advanced Implementation of the KeyLock which allows you to setup complicated conflict dependencies.
 * But it requires you to use {@link IKey} as the Key and that the Keys are generated by the internal Generator.
 */
public class ConflictingLock implements IKeyLock<IKey<?>> {
	
	public static final Object TRUE = new Object();
	
	public IndexMap<LockLikeSignalable> holders;
	public IndexMultiMap<Object> conflictMap;
	public IKeyGenerator gen;
	public int size;
	
	public ConflictingLock(IKeyGenerator gen, int size) {
		this(new IndexMapArray<>(), new IndexMultiMap2D<>(), gen, size);
	}
	
	public ConflictingLock(IndexMap<LockLikeSignalable> holders, IndexMultiMap<Object> conflictMap, IKeyGenerator gen, int size) {
		this.holders = holders;
		this.conflictMap = conflictMap;
		this.gen = gen;
		this.size = size;
	}
	
	//methods
	public void check(IKey<?> key) {
		if (gen != null && !gen.isKeyOf(key))
			throw new IllegalKeyException();
		if (key.getID() >= size)
			throw new IllegalStateException();
	}
	
	public void ensureKeyCapacity(int newSize) {
		if (newSize >= size)
			return;
		for (int i = size; i < newSize; i++)
			holders.put(i, new LockLikeSignalable());
		size = newSize;
	}
	
	public void ensureKeyCapacityIndex(int newSize) {
		ensureKeyCapacity(newSize + 1);
	}
	
	public IKeyGenerator makeModifiedKeyGenerator() {
		return gen.whenGenerated(key -> ensureKeyCapacityIndex(key.getID()));
	}
	
	//conflict
	
	/**
	 * will add a conflict
	 *
	 * @param acquiring the {@link IKey} trying to acquire the lock
	 * @param holding   the {@link IKey} currently holding the lock, which will block any acquire attempts when {@link IKey} is acquiring
	 */
	public void addConflict(IKey<?> acquiring, IKey<?> holding) {
		setConflict(acquiring, holding, true);
	}
	
	/**
	 * will remove a conflict
	 *
	 * @param acquiring the {@link IKey} trying to acquire the lock
	 * @param holding   the {@link IKey} currently holding the lock, which will block any acquire attempts when {@link IKey} is acquiring
	 */
	public void removeConflict(IKey<?> acquiring, IKey<?> holding) {
		setConflict(acquiring, holding, false);
	}
	
	/**
	 * will set a conflict's existence
	 *
	 * @param acquiring the {@link IKey} trying to acquire the lock
	 * @param holding   the {@link IKey} currently holding the lock, which will block any acquire attempts when {@link IKey} is acquiring
	 */
	public void setConflict(IKey<?> acquiring, IKey<?> holding, boolean value) {
		synchronized (this) {
			check(acquiring);
			check(holding);
			if (value) {
				conflictMap.put(new int[] {acquiring.getID(), holding.getID()}, TRUE);
			} else {
				conflictMap.remove(new int[] {acquiring.getID(), holding.getID()});
			}
		}
	}
	
	//conflict Bidirectional
	
	/**
	 * will add a conflict bidirectional
	 */
	public void addConflictBidirectional(IKey<?> key1, IKey<?> key2) {
		setConflictBidirectional(key1, key2, true);
	}
	
	/**
	 * will remove a conflict bidirectional
	 */
	public void removeConflictBidirectional(IKey<?> key1, IKey<?> key2) {
		setConflictBidirectional(key1, key2, false);
	}
	
	/**
	 * will set a conflict's existence bidirectional
	 */
	public void setConflictBidirectional(IKey<?> key1, IKey<?> key2, boolean value) {
		setConflict(key1, key2, value);
		setConflict(key2, key1, value);
	}
	
	//conflict self
	
	/**
	 * will add a conflict bidirectional
	 */
	public void addSelfConflict(IKey<?> key) {
		setConflictBidirectional(key, key, true);
	}
	
	/**
	 * will remove a conflict bidirectional
	 */
	public void removeSelfConflict(IKey<?> key) {
		setConflictBidirectional(key, key, false);
	}
	
	/**
	 * will set a conflict's existence bidirectional
	 */
	public void setSelfConflict(IKey<?> key, boolean value) {
		setConflict(key, key, value);
	}
	
	//lock
	@Override
	public void lock(IKey<?> key) {
		check(key);
		while (true) {
			IAwaitable await;
			synchronized (this) {
				await = internalCheckLock(key);
				if (await == null) {
					internalForceLock(key);
					return;
				}
			}
			
			try {
				await.await();
			} catch (InterruptedException ignore) {
			
			}
		}
	}
	
	@Override
	public void lock(IKey<?> key, long time, TimeUnit unit) {
		check(key);
		while (true) {
			IAwaitable await;
			synchronized (this) {
				await = internalCheckLock(key);
				if (await == null) {
					internalForceLock(key);
					return;
				}
			}
			
			try {
				await.await(time, unit);
			} catch (InterruptedException ignore) {
			
			}
		}
	}
	
	@Override
	public void lockInterruptibly(IKey<?> key) throws InterruptedException {
		check(key);
		while (true) {
			IAwaitable await;
			synchronized (this) {
				await = internalCheckLock(key);
				if (await == null) {
					internalForceLock(key);
					return;
				}
			}
			await.await();
		}
	}
	
	@Override
	public synchronized void lockInterruptibly(IKey<?> key, long time, TimeUnit unit) throws InterruptedException {
		check(key);
		while (true) {
			IAwaitable await;
			synchronized (this) {
				await = internalCheckLock(key);
				if (await == null) {
					internalForceLock(key);
					return;
				}
			}
			await.await(time, unit);
		}
	}
	
	@Override
	public synchronized boolean tryLock(IKey<?> key) {
		check(key);
		IAwaitable await;
		synchronized (this) {
			await = internalCheckLock(key);
			if (await == null) {
				internalForceLock(key);
				return true;
			}
		}
		return false;
	}
	
	//unlock
	@Override
	public synchronized void unlock(IKey<?> key) {
		check(key);
		internalForceUnlock(key);
	}
	
	//unsupported
	@Override
	public synchronized boolean tryUnlock(IKey<?> key) {
		throw new UnsupportedOperationException();
	}
	
	@Override
	@Deprecated
	public synchronized boolean isLocked() {
		throw new UnsupportedOperationException();
	}
	
	@Override
	@Deprecated
	public IKey<?> getHolder() {
		throw new UnsupportedOperationException();
	}
	
	//private
	protected IAwaitable internalCheckLock(IKey<?> key) {
		int id = key.getID();
		int[] pos = new int[2];
		pos[0] = id;
		
		for (int i = 0; i < size; i++) {
			pos[1] = i;
			Object conf = conflictMap.get(pos);
			if (conf == null)
				continue;
			LockLikeSignalable await = holders.get(i);
			if (await.isSignaled())
				continue;
			return await;
		}
		return null;
	}
	
	protected void internalForceLock(IKey<?> key) {
		holders.get(key.getID()).reset();
	}
	
	protected void internalForceUnlock(IKey<?> key) {
		holders.get(key.getID()).signal();
	}
}
